from Graph import Graph


def main():
    g = Graph(4)
    g.add_edge(0, 1)
    g.add_edge(0, 2)
    g.add_edge(1, 2)
    g.add_edge(1, 3)
    g.add_edge(2, 3)
    nodes = g.traverse_dfs()
    for i in nodes:
        print(i)


# Press the green button in the gutter to run the script.
if __name__ == '__main__':
    main()

